package day04_Variables;

public class PrimitivesContinue {

    public static void main(String[] args) {

        char ch1 = '#';
        char ch2 = 35;

        System.out.println("ch1 = " + ch1);
        System.out.println("ch2 = " + ch2);

        char ch3 = 'Z';
        char ch4 = 90;

        System.out.println("ch3 = " + ch3);
        System.out.println("ch4 = " + ch4);


    }
}
