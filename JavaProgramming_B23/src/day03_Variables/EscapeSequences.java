package day03_Variables;
/*
    \n: new line
    \t: paragraph space (tab)
    * */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello Everyone \nHow are you?");
        System.out.println("------------------------------------");
        System.out.println("Hello Everyone");
        System.out.println("I am Collins");
        System.out.println("I live in Nairobi");
        System.out.println("-------------------------------------");
        System.out.println("Hello everyone \nI am Collins \nI live in Nairobi");

        System.out.println("-------------------------------------------");

        System.out.println("\tToday is a great day to study Java");

        System.out.println("-------------------------------------------");

        System.out.println("My favorite TV series is \"Game of throne\"");
    }


}
