package day19_loops;

public class Shape2 {
    public static void main(String[] args) {
        System.out.println("* * * * * *");
        for (int i = 1; i <= 8 ; i++) {
            System.out.println("*         *");
        }
        System.out.println("* * * * * *");

    }
}
