package day05_ArithmeticOperators;

public class DisionCalculator {

    public static void main(String[] args) {

        int num1 = 20;
        int num2 = 5;
        int result = num1/num2;
        int remainder = num1%num2;
        System.out.println(num1 +" divide by "+ num2 + " is equal to "+ result +
                " and remainder of " + remainder);
    }
}
